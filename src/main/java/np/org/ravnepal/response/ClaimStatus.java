package np.org.ravnepal.response;

public class ClaimStatus {
	
	boolean codingComplete;
	boolean saveForLater;
	
	
	public boolean isCodingComplete() {
		return codingComplete;
	}
	public void setCodingComplete(boolean codingComplete) {
		this.codingComplete = codingComplete;
	}
	public boolean isSaveForLater() {
		return saveForLater;
	}
	public void setSaveForLater(boolean saveForLater) {
		this.saveForLater = saveForLater;
	}
	
	

}
